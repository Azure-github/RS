const supportedLanguages = ['de', 'hu'];
const defaultLanguage = 'de';

function getBrowserLanguage() {
  const lang = navigator.language || navigator.userLanguage || defaultLanguage;
  const code = lang.toLowerCase().slice(0, 2);
  return supportedLanguages.includes(code) ? code : defaultLanguage;
}

function getSavedLanguage() {
  try {
    return localStorage.getItem('site-language');
  } catch {
    return null;
  }
}

function saveLanguage(lang) {
  try {
    localStorage.setItem('site-language', lang);
  } catch {
    // ignore localStorage errors
  }
}

function applyLanguage(lang) {
  const language = supportedLanguages.includes(lang) ? lang : defaultLanguage;
  document.documentElement.lang = language;
  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const translated = translations[language] && translations[language][key];
    element.textContent = translated || translations[defaultLanguage][key] || element.textContent;
  });
  document.querySelectorAll('.lang-switcher button').forEach((button) => {
    button.classList.toggle('active', button.dataset.lang === language);
  });
}

function initLanguageSwitcher() {
  document.querySelectorAll('.lang-switcher button').forEach((button) => {
    button.addEventListener('click', () => {
      const lang = button.dataset.lang;
      applyLanguage(lang);
      saveLanguage(lang);
    });
  });
}

function initScrollSpy() {
  const navLinks = document.querySelectorAll('.main-nav a');
  const sections = document.querySelectorAll('section[id]');

  function updateActiveLink() {
    const scrollY = window.scrollY + window.innerHeight / 2; // Center of viewport
    let minDistance = Infinity;
    let current = '';

    sections.forEach((section) => {
      const sectionCenter = section.offsetTop + section.offsetHeight / 2;
      const distance = Math.abs(sectionCenter - scrollY);
      if (distance < minDistance) {
        minDistance = distance;
        current = section.getAttribute('id');
      }
    });

    navLinks.forEach((link) => {
      link.classList.remove('active');
      if (link.getAttribute('href') === '#' + current) {
        link.classList.add('active');
      }
    });
  }

  window.addEventListener('scroll', updateActiveLink);
  updateActiveLink(); // Initial call
}

function initServiceAccordion() {
  document.querySelectorAll('.service-grid article, .reference-grid article').forEach((article) => {
    article.addEventListener('click', () => {
      article.classList.toggle('expanded');
    });
  });
}

function initLocalization() {
  const savedLang = getSavedLanguage();
  const initialLanguage = savedLang || getBrowserLanguage();
  applyLanguage(initialLanguage);
  if (!savedLang) {
    saveLanguage(initialLanguage);
  }
}

function initMobileMenu() {
  const toggle = document.querySelector('.menu-toggle');
  const nav = document.getElementById('main-nav');
  if (!toggle || !nav) return;

  function getMenuLabel(isOpen) {
    const language = document.documentElement.lang;
    if (language === 'hu') {
      return isOpen ? 'Menü bezárása' : 'Menü megnyitása';
    }
    return isOpen ? 'Menü schließen' : 'Menü öffnen';
  }

  function setMenuState(isOpen) {
    nav.classList.toggle('open', isOpen);
    toggle.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
    toggle.setAttribute('aria-label', getMenuLabel(isOpen));
  }

  setMenuState(false);

  toggle.addEventListener('click', () => {
    const isOpen = !nav.classList.contains('open');
    setMenuState(isOpen);
  });

  nav.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      setMenuState(false);
    });
  });

  document.querySelectorAll('.lang-switcher button').forEach((button) => {
    button.addEventListener('click', () => {
      window.setTimeout(() => {
        const isOpen = nav.classList.contains('open');
        toggle.setAttribute('aria-label', getMenuLabel(isOpen));
      }, 0);
    });
  });
}

initLanguageSwitcher();
initScrollSpy();
initServiceAccordion();
initLocalization();
initMobileMenu();
